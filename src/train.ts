function objectToArray(obj: Record<string, any>): { [key: string]: any }[] {
    return Object.entries(obj).reduce((acc, [key, value]) => {
      acc.push({ [key]: value });
      return acc;
    }, [] as { [key: string]: any }[]);
  }
  
  
const myObject = { a: 20, b: 40 };
const array = objectToArray(myObject);
console.log(array); // Output: [{ a: 20 }, { b: 40 }]

